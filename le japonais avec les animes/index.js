kanjis.forEach(function(clic, i){
  clic.addEventListener("click", function(){

  for (var j = 0; j < vocabulaire.length; j++) {

    if(kanjis[i].innerHTML == vocabulaire[j].mot){
      document.querySelector(".container").style.display = "block";
      document.querySelector(".info").innerHTML += "<h3>" + vocabulaire[j].transcription + "</h3><span class='furigana'>" + vocabulaire[j].furigana + "</span><p>" + vocabulaire[j].traduction + "</p>";
    }
  }

  })
})

// Fin Remplis div .info
document.querySelector(".container").addEventListener("click", function(){
  document.querySelector(".container").style.display = "none";
  document.querySelector(".info").innerHTML = "";
});
